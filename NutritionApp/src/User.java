public class User {

    private String username;
    private float weight;
    private float height;
    private int activityLevel;
    private int age;

    public User(String username, float weight, float height, int activityLevel, int age) {
        this.username = username;
        this.weight = weight;
        this.height = height;
        this.activityLevel = activityLevel;
        this.age = age;
    }
}
